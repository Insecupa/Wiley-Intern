//Abstract classes
//Abstract class cannot be instantiated 
//Any incomplete 
//It can have all or none as abstract
//Abstract can have a constructor
public abstract class Animal {
	
	public Animal() {
		System.out.println("This is Animal Class");
	}
	
	public void talk() {
		System.out.println("Animal talking generically");
	}
	
	public void eat() {
		System.out.println("Animal eating generical  ly");
	}
	
	public void sleep() {
		System.out.println("Animal sleeping generically");
	}
	
	public void breath() {
		System.out.println("Animal breathing generically");
	}
	
}
